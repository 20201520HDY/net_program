text = "My name is Handongyeob"

print("Number of characters:", len(text))

print("Repeated 10 times:", text * 10)

print("First character:", text[0])

print("First 4 characters:", text[:4])

print("Last 4 characters:", text[-4:])

print("Reversed string:", text[::-1])

print("Without first and last character:", text[1:-1])

print("Uppercase:", text.upper())

print("Lowercase:", text.lower())

print("Replace 'a' with 'e':", text.replace('a', 'e'))
